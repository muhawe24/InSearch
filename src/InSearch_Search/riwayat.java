        public void populateJTable(){
        QueryRiwayat q = new QueryRiwayat();
        //String cari = kolomCari.getText();
        ArrayList<buku> list = q.BindTable();
        String[] columnName = {"Gambar","ID","Judul","Stok","Letak","Waktu Akses"};
        Object[][] rows = new Object[list.size()][7];
        for(int i = 0; i < list.size(); i++){
            if(list.get(i).getImage() != null){
                ImageIcon image = new ImageIcon(new ImageIcon(list.get(i).getImage()).getImage()
                .getScaledInstance(100, 100, Image.SCALE_SMOOTH));
                
            rows[i][0] = image;
            }
            else{
                rows[i][0] = null;
            }
            rows[i][1] = list.get(i).getId_buku();
            rows[i][2] = list.get(i).getJudul();
            rows[i][3] = list.get(i).getStok();
            rows[i][4] = list.get(i).getLetak();
            rows[i][5] = QueryRiwayat.list_waktu.get(i);
        }
        
        Model mod = new Model(columnName, rows);
        jTable1.setModel(mod);
        jTable1.setRowHeight(120);
        jTable1.getColumnModel().getColumn(0).setPreferredWidth(150);
        
    }

